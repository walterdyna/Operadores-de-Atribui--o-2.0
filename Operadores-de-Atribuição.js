/*

Operadores de Atribuição

 = -> Atribuição
+= -> Adição
-= -> Subtração
/= -> Divisão
*= -> Multiplicação
%= -> Módulo (Resto da Divisão)

*/

const result = 20 // atibuição de valor ou texto

result = result + 70 
// e igual
result += 70 // mesma coisa que a linha acima (codigo mais bonito)
console.log(result) // imprime o valor de result que é 90

result = result - 70
// e igual
result -= 70 // mesma coisa que a linha acima (codigo mais bonito)
console.log(result) // imprime o valor de result que é 20

result = result / 70
// e igual
result /= 70 // mesma coisa que a linha acima (codigo mais bonito)
console.log(result) // imprime o valor de result que é 0.285714285714

result = result * 70
// e igual
result *= 70 // mesma coisa que a linha acima (codigo mais bonito)
console.log(result) // imprime o valor de result que é 20

result = result % 70
// e igual
result %= 2 // mesma coisa que a linha acima (codigo mais bonito)
console.log(result) // imprime o valor de result que é 0

